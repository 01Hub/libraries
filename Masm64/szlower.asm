align 8

szLower proc FRAME text:QWORD

  ; -----------------------------
  ; converts string to lower case
  ; invoke szLower,ADDR szString
  ; -----------------------------

    mov rax, text
    dec rax

  @@:
    add rax, 1
    cmp BYTE PTR [rax], 0
    je @F
    cmp BYTE PTR [rax], "A"
    jb @B
    cmp BYTE PTR [rax], "Z"
    ja @B
    add BYTE PTR [rax], 32
    jmp @B
  @@:

    mov rax, text

    ret

szLower endp


